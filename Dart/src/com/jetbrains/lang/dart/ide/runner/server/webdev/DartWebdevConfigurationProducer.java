package com.jetbrains.lang.dart.ide.runner.server.webdev;

import com.intellij.execution.actions.ConfigurationContext;
import com.intellij.execution.actions.LazyRunConfigurationProducer;
import com.intellij.execution.configurations.ConfigurationFactory;
import com.intellij.openapi.project.Project;
import com.intellij.openapi.roots.ProjectFileIndex;
import com.intellij.openapi.roots.ProjectRootManager;
import com.intellij.openapi.util.Ref;
import com.intellij.openapi.vfs.VirtualFile;
import com.intellij.psi.PsiElement;
import com.intellij.psi.PsiFile;
import com.intellij.psi.impl.source.html.HtmlLikeFile;
import com.jetbrains.lang.dart.analyzer.DartAnalysisServerService;
import com.jetbrains.lang.dart.sdk.DartSdk;
import com.jetbrains.lang.dart.util.PubspecYamlUtil;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;

public final class DartWebdevConfigurationProducer extends LazyRunConfigurationProducer<DartWebdevConfiguration> {
  @NotNull
  @Override
  public ConfigurationFactory getConfigurationFactory() {
    return DartWebdevConfigurationType.getInstance().getConfigurationFactories()[0];
  }

  @Override
  protected boolean setupConfigurationFromContext(final @NotNull DartWebdevConfiguration configuration,
                                                  final @NotNull ConfigurationContext context,
                                                  final @NotNull Ref<PsiElement> sourceElement) {
    final VirtualFile htmlFile = getRunnableHtmlFileFromContext(context);
    if (htmlFile == null) {
      return false;
    }

    configuration.getParameters().setHtmlFilePath(htmlFile.getPath());
    configuration.setGeneratedName();
    sourceElement.set(sourceElement.isNull() ? null : sourceElement.get().getContainingFile());
    return true;
  }

  @Override
  public boolean isConfigurationFromContext(final @NotNull DartWebdevConfiguration configuration,
                                            final @NotNull ConfigurationContext context) {
    final Project project = context.getProject();
    if (project == null) {
      return false;
    }
    final VirtualFile htmlFile = getHtmlFileFromContext(context, project);
    return htmlFile != null && htmlFile.getPath().equals(configuration.getParameters().getHtmlFilePath());
  }

  /**
   * This method itself, as well as helper methods such as {{@link #getHtmlFileFromContext(ConfigurationContext, Project)}, it is important
   * to have a superset of the verifications that are executed in {@link DartWebdevParameters#check(Project)} so that run configurations
   * generated by this producer don't immediately hit errors generated by validating the parameters in {@link DartWebdevParameters}.
   */
  @Nullable
  private static VirtualFile getRunnableHtmlFileFromContext(@NotNull final ConfigurationContext context) {
    final Project project = context.getProject();
    if (project == null) {
      return null;
    }

    // Check the version of the Dart SDK
    final DartSdk dartSdk = DartSdk.getDartSdk(project);
    if (dartSdk == null || dartSdk.getVersion().isEmpty() || !DartAnalysisServerService.isDartSdkVersionSufficientForWebdev(dartSdk)) {
      return null;
    }

    final VirtualFile htmlVirtualFile = getHtmlFileFromContext(context, project);
    if (htmlVirtualFile == null) {
      return null;
    }

    final ProjectFileIndex projectFileIndex = ProjectRootManager.getInstance(project).getFileIndex();
    return projectFileIndex.isInContent(htmlVirtualFile) ? htmlVirtualFile : null;
  }

  @Nullable
  private static VirtualFile getHtmlFileFromContext(@NotNull final ConfigurationContext context, @NotNull final Project project) {
    final PsiElement psiLocation = context.getPsiLocation();
    final PsiFile psiFile = psiLocation == null ? null : psiLocation.getContainingFile();
    final VirtualFile htmlFile = psiFile == null ? null : psiFile.getVirtualFile();
    if (psiFile instanceof HtmlLikeFile && htmlFile != null && htmlFile.exists()) {
      // Only return this htmlFile, VirtualFile if the html file is in a Dart project, defined by
      // - finding a pubspec yaml file,
      // - and the html file is not in the same directory as the found pubspec.
      VirtualFile pubspecFile = PubspecYamlUtil.findPubspecYamlFile(project, htmlFile);
      if (pubspecFile == null) {
        return null;
      }
      VirtualFile dartProjectRoot = pubspecFile.getParent();
      if (htmlFile.getParent().equals(dartProjectRoot)) {
        return null;
      }
      return htmlFile;
    }
    return null;
  }
}
